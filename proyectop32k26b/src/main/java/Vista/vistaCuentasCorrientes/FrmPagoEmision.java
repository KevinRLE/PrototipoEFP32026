    private void llenarTabla(List<clsPagosEmision> lista) {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Cuenta por Pagar");
    modelo.addColumn("Movimiento");
    modelo.addColumn("Fecha");
    modelo.addColumn("Monto");

    for (clsPagosEmision pago : lista) {
        Object[] fila = {
            pago.getPagemid(),
            pago.getCppcodigo(),
            pago.getMovbid(),
            pago.getPagefecha(),
            pago.getPagemonto()
        };
        modelo.addRow(fila);
    }
    jTable1.setModel(modelo);
}

