    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String tipo = jComboBox1.getSelectedItem().toString();//Lee el comboBox
        if (jTextField1.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Ingrese el código de la cuenta por pagar.",
            "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int codigoCxP = Integer.parseInt(jTextField1.getText().trim());
        PagoEmisionDAO dao = new PagoEmisionDAO();

        if (tipo.equals("Acreedor")) {
            // Modo automático: jala el saldo automáticamente
            double saldo = dao.obtenerSaldo(codigoCxP);

            if (saldo == 0) {
                JOptionPane.showMessageDialog(this,
                    "No se encontró saldo para ese código de acreedor.",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            jTextField2.setText(String.valueOf(saldo)); // Saldo Pendiente
            //jTextField3.setEditable(false); // Monto a Pagar bloqueado (o puedes dejarlo editable)
            //jTextField4.setEditable(false); // Tipo de Pago bloqueado
            // Si quieres autocompletar Monto a Pagar con el saldo total:
            //jTextField3.setText(String.valueOf(saldo));

        } else {
            // Modo manual (Proveedor): el usuario ingresa todo
            jTextField2.setText(""); // Limpia saldo
            jTextField3.setText(""); // Limpia monto
            jTextField3.setEditable(true);
            jTextField4.setEditable(true);
        }

        // En ambos casos, llenar la tabla con pagos anteriores
        llenarTabla(dao.selectPorCuenta(codigoCxP));

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
            "El código debe ser un número válido.",
            "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
            "Error: " + e.getMessage(),
            "ERROR", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() ||
        jTextField4.getText().trim().isEmpty() ||
        jTextField5.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "No pueden haber campos vacíos.",
            "ERROR", JOptionPane.ERROR_MESSAGE);
        return;
    }
        try {
        double montoPago= Double.parseDouble(jTextField3.getText().trim());
        double saldoActual= Double.parseDouble(jTextField2.getText().trim());
        
        //Validación de que el monto no sea mayor al saldo
        if (montoPago > saldoActual){
            JOptionPane.showMessageDialog(this,
                    "El monto a pagar no puede ser mayor al saldo pendiente.",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        clsPagosEmision pago = new clsPagosEmision();
        pago.setCppcodigo(Integer.parseInt(jTextField1.getText().trim()));
        pago.setMovbid(Integer.parseInt(jTextField5.getText().trim()));
        pago.setPagemonto(montoPago);
        pago.setPagetipo(jTextField4.getText().trim());
        
        PagoEmisionDAO dao = new PagoEmisionDAO();
        int resultado = dao.registrarPago(pago);
        
        if (resultado > 0){
            BitacoraDAO bitacoraDAO = new BitacoraDAO();
            bitacoraDAO.insert(clsUsuarioConectado.getUsuId(), 40010, "Pago Emisión");
            
            JOptionPane.showMessageDialog(this, 
                    "Pago registrado correctamente.",
                    "Exito", JOptionPane.INFORMATION_MESSAGE);
            jButton3ActionPerformed(evt);
        } else{
            JOptionPane.showMessageDialog(this,
                    "No se pudo registrar pago.",
                    "ERROR", JOptionPane.ERROR_MESSAGE);
            }
    }catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "ERROR", JOptionPane.ERROR_MESSAGE);
      }
        
    }//GEN-LAST:event_jButton2ActionPerformed

